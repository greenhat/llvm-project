#ifndef LLVM_LIB_TARGET_MIDEN_MIDENSUBTARGET_H
#define LLVM_LIB_TARGET_MIDEN_MIDENSUBTARGET_H

#include "MidenFrameLowering.h"
#include "MidenISelLowering.h"
#include "MidenInstrInfo.h"
#include "MidenSelectionDAGInfo.h"
#include "llvm/CodeGen/TargetSubtargetInfo.h"
#include <string>

#define GET_SUBTARGETINFO_ENUM
#define GET_SUBTARGETINFO_HEADER
#include "MidenGenSubtargetInfo.inc"

namespace llvm {

// Defined in MidenGenSubtargetInfo.inc.
extern const SubtargetFeatureKV MidenFeatureKV[Miden::NumSubtargetFeatures];

class MidenSubtarget final : public MidenGenSubtargetInfo {
  enum SIMDEnum {
    NoSIMD,
    SIMD128,
    RelaxedSIMD,
  } SIMDLevel = NoSIMD;

  bool HasAtomics = false;
  bool HasNontrappingFPToInt = false;
  bool HasSignExt = false;
  bool HasExceptionHandling = false;
  bool HasBulkMemory = false;
  bool HasMultivalue = false;
  bool HasMutableGlobals = false;
  bool HasTailCall = false;
  bool HasReferenceTypes = false;
  bool HasExtendedConst = false;

  /// What processor and OS we're targeting.
  Triple TargetTriple;

  MidenFrameLowering FrameLowering;
  MidenInstrInfo InstrInfo;
  MidenSelectionDAGInfo TSInfo;
  MidenTargetLowering TLInfo;

  MidenSubtarget &initializeSubtargetDependencies(StringRef CPU, StringRef FS);

public:
  /// This constructor initializes the data members to match that
  /// of the specified triple.
  MidenSubtarget(const Triple &TT, const std::string &CPU,
                 const std::string &FS, const TargetMachine &TM);

  const MidenSelectionDAGInfo *getSelectionDAGInfo() const override {
    return &TSInfo;
  }
  const MidenFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }
  const MidenTargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }
  const MidenInstrInfo *getInstrInfo() const override { return &InstrInfo; }
  const MidenRegisterInfo *getRegisterInfo() const override {
    return &getInstrInfo()->getRegisterInfo();
  }
  const Triple &getTargetTriple() const { return TargetTriple; }
  bool enableAtomicExpand() const override;
  bool enableIndirectBrExpand() const override { return true; }
  bool enableMachineScheduler() const override;
  bool useAA() const override;

  // Predicates used by MidenInstrInfo.td.
  bool hasAddr64() const { return TargetTriple.isArch64Bit(); }
  bool hasSIMD128() const { return SIMDLevel >= SIMD128; }
  bool hasRelaxedSIMD() const { return SIMDLevel >= RelaxedSIMD; }
  bool hasAtomics() const { return HasAtomics; }
  bool hasNontrappingFPToInt() const { return HasNontrappingFPToInt; }
  bool hasSignExt() const { return HasSignExt; }
  bool hasExceptionHandling() const { return HasExceptionHandling; }
  bool hasBulkMemory() const { return HasBulkMemory; }
  bool hasMultivalue() const { return HasMultivalue; }
  bool hasMutableGlobals() const { return HasMutableGlobals; }
  bool hasTailCall() const { return HasTailCall; }
  bool hasReferenceTypes() const { return HasReferenceTypes; }

  /// Parses features string setting specified subtarget options. Definition of
  /// function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef TuneCPU, StringRef FS);
};

} // end namespace llvm

#endif
